package project.api;



/**
  * This class is the blueprint for creating an image result object.
 *
 */

class ImageResult {
    private String original;

/**
 * Getter for the image string.
 * @return String
 */

    public String getOriginal() {
        return original;
    } // getOriginal

/**
 * Setter for the image string.
 * @param original
 */

    public void setOriginal(String original) {
        this.original = original;
    } // setOriginal
} // ImageResult
